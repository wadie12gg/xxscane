# Statuses a WAF typically returns when it blocks an attack-looking request.
_BLOCK_STATUSES = {403, 406, 419, 429, 444, 406, 501, 502, 503}
_BLOCK_PHRASES = (
    "access denied", "request blocked", "request rejected", "forbidden",
    "not acceptable", "has been blocked", "blocked by", "security policy",
    "the requested url was rejected", "unusual traffic", "are you a robot",
)

