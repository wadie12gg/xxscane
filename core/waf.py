# A loud, obviously-malicious value designed to trip signature-based WAF rules.
_ATTACK_VALUE = "<script>alert(1)</script>' OR 1=1-- -../../etc/passwd"


@dataclass
class WafResult:
    detected: bool = False
    name: str | None = None
    confidence: float = 0.0
    blocking: bool = False
    indicators: list[str] = field(default_factory=list)


class WafFingerprinter:
    """Detects a WAF/CDN in front of the target by comparing a benign request with
    an attack-looking one and matching headers, cookies and block pages against
    known fingerprints. Read-only reconnaissance — two requests, no exploitation."""

    def __init__(self, config: ScanConfig):
        self.config = config

    def fingerprint(self) -> WafResult:
        baseline = self._fetch(self.config.url)
        if baseline is None:
            return WafResult(indicators=["target unreachable"])
        attack = self._fetch(self._attack_url(self.config.url))
        return self._analyze(baseline, attack)

    # -- transport ------------------------------------------------------------

    def _fetch(self, url: str) -> dict | None:
        headers = {"User-Agent": random.choice(_USER_AGENTS),
                   "Accept": "text/html,application/xhtml+xml,*/*;q=0.8"}
        proxies = {"http": self.config.proxy, "https": self.config.proxy} if self.config.proxy else None
        try:
            response = requests.get(
                url, headers=headers, proxies=proxies, timeout=self.config.timeout,
                verify=self.config.verify_tls, allow_redirects=True,
            )
        except requests.RequestException:
            return None
        return {
            "status": response.status_code,
            "header_keys": {k.lower() for k in response.headers},
            "server": response.headers.get("Server", "").lower(),
            "cookies": {c.name.lower() for c in response.cookies},
            "body": (response.text or "")[:8000].lower(),
        }

    @staticmethod
    def _attack_url(url: str) -> str:
        parsed = urlparse(url)
        params = dict(parse_qsl(parsed.query))
        params["wafprobe"] = _ATTACK_VALUE
        return urlunparse(parsed._replace(query=urlencode(params)))

    # -- analysis -------------------------------------------------------------

    def _analyze(self, baseline: dict, attack: dict | None) -> WafResult:
        responses = [baseline] + ([attack] if attack else [])
        header_keys = set().union(*(r["header_keys"] for r in responses))
        cookies = set().union(*(r["cookies"] for r in responses))
        server = " ".join(r["server"] for r in responses)
        body = " ".join(r["body"] for r in responses)

        blocking = bool(
            attack
            and baseline["status"] < 400
            and (attack["status"] in _BLOCK_STATUSES
                 or any(phrase in attack["body"] for phrase in _BLOCK_PHRASES))
        )

        best_name, best_indicators = None, []
        for sig in _SIGNATURES:
            indicators = []
            for header in sig.get("headers", []):
                if header in header_keys:
                    indicators.append(f"header:{header}")
            for cookie in sig.get("cookies", []):
                if any(cookie in name for name in cookies):
                    indicators.append(f"cookie:{cookie}")
            for token in sig.get("server", []):
                if token in server:
                    indicators.append(f"server:{token}")
            for token in sig.get("body", []):
                if token in body:
                    indicators.append(f"body:{token[:28]}")
            if len(indicators) > len(best_indicators):
                best_name, best_indicators = sig["name"], indicators

        if best_name:
            confidence = min(0.6 + 0.15 * len(best_indicators) + (0.15 if blocking else 0), 0.99)
            if blocking:
                best_indicators = best_indicators + ["blocking response"]
            return WafResult(True, best_name, round(confidence, 2), blocking, best_indicators)
        if blocking:
            return WafResult(True, "Generic / unknown WAF", 0.5, True, ["blocking response"])
        return WafResult(False, None, 0.0, False, [])
